export type Messages = typeof zhCNMessages;

type DotJoin<Prefix extends string, Key extends string> = Prefix extends "" ? Key : `${Prefix}.${Key}`;

type NestedTranslationKey<T, Prefix extends string = ""> = {
  [Key in keyof T & string]: T[Key] extends string
    ? DotJoin<Prefix, Key>
    : T[Key] extends Record<string, unknown>
      ? NestedTranslationKey<T[Key], DotJoin<Prefix, Key>>
      : never;
}[keyof T & string];

export type TranslationKey = NestedTranslationKey<Messages>;
export type TranslationParams = Record<string, string | number>;
export type TranslateFn = (key: TranslationKey, params?: TranslationParams) => string;
